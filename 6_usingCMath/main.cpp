#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout << "max: " << max(5,10) << endl;
    cout << "min: " << min(5,10) << endl;
    cout << "sqrt: " << sqrt(64) << endl;
    cout << "round: " << round(2.6) << endl;
    cout << "fmax: " << fmax(2.1,2.2) << endl;
    cout << "abs: " << abs(-10);
    return 0;
}
