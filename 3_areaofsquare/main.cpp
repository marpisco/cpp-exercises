#include <iostream>
using namespace std;

int main()
{
    int base, height;
    cout << "Let's calculate the area of the square plot!" << endl;
    cout << "Enter the base: " << endl;
    cin >> base;
    cout << "Enter the height: " << endl;
    cin >> height;
    cout << "The area of your plot is: " << (base * height) << endl;
    return 0;
}